# slicing with two positions
b = "Sai satish"
slice1 = b[4:9] # slicing from 0th position to 1st position, it will not 2nd position
print(slice1)


# slicing from start

c = "Gautam Kushwaha"
slice2 = c[:5]
print(slice2)

# slicing to the end
d = "IIT Tirupati"
slice3= d[2:]
print(slice3)


#negative slicing

e = "Ram was a great king"
invert = e[-3]
print(invert)
slice4 = e[-9:-2]
print(slice4)
# print the number from 1 to 15 using while loop

# syntax of while loop is : while <condition>:   if <condition> is true the inside statement of the while loop will work otherwise the inside statemment of the while loop will not work

i=0
while i<16:
    print(i)
    i = i+1  # here i = i+1 will increase the value of i in each iteration by 1 measn in first iteration the value of i will be 0, then 1, then 2,.....15